-- HIVESQL
select *,
months_between(from_unixtime(unix_timestamp(end_dt, 'yyyyMMdd'), 'yyyy-MM-dd')
            , from_unixtime(unix_timestamp(start_dt, 'yyyy-MM-dd'), 'yyyy-MM-dd')) as months_diff_hive
from time_function_test

-- PRESTOSQL
WITH tmp1 AS (
    select *
    , cast(data_parse(start_dt, '%Y%m%d') AS date) AS d2
    , cast(data_parse(end_dt, '%Y%m%d') AS date) AS d1
    , date_add('day', -1, date_add('month', 1, date_trunc('month', cast(data_parse(start_dt, '%Y%m%d') AS date)))) AS last_day_of_d2
    , date_add('day', -1, date_add('month', 1, date_trunc('month', cast(data_parse(end_dt, '%Y%m%d') AS date)))) AS last_day_of_d1
    from time_function_test
)
select *
, case
    when day(d1) = day(d2) then (d1=last_day_of_d1 and d2=last_day_of_d2) then cast(((year(d1) - year(d2)) * 12 + month(d1) - month(d2)) AS double)
    else cast(((year(d1) - year(d2)) * 12 + month(d1) - month(d2)) AS double) + (day(d1) - day(d2)) / 31.00000000
    end as months_diff_presto
from tmp1
;